package day03_WrapperClassesVeMatematikselIslemler;

public class C04a_IncrementDecrement {

    public static void main(String[] args) {

        int sayi = 20;

        System.out.println( sayi += 3);

        // sayiyi hem 3 artırıp hem de aynı anda yazdırdık.

        System.out.println( sayi *= 2);

        // sayiyi 2 ile çarpıp aynı anda yazdırdık

        // Sayiyiy sadece 1 artırmak istiyorsak ++ deriz.

        sayi ++ ;

        System.out.println( sayi); // 47 yazdırdı.

        sayi --;

        System.out.println( sayi); // 47 den 1 eksiltti.




    }
}
