package day03_WrapperClassesVeMatematikselIslemler;

public class C04_Increment {

    public static void main(String[] args) {

        int sayi = 10;

        sayi = sayi +3 ;

        sayi += 3;

        // sayının değerini 3 artırmak istersek bu şekilde yazabiliriz

        sayi *= 3;

        // 3 ile çarpar.

        System.out.println(sayi);


    }
}
