package day03_WrapperClassesVeMatematikselIslemler;

public class C02_Modulus {

    public static void main(String[] args) {

        System.out.println(85 % 6);
        //1 verdi. Yani 85'in 6'ya bölümünden kalanı verir % işlemi.

        System.out.println(283 % 10 ); //birler basamağını bulmak için daima 10'a böleriz
        // 3 verir

        System.out.println( 283 % 2); // 283 tek mi çift mi?
        // bu işlem sonucu 1 ise tek sayıdır deriz.

        System.out.println( 347 % 5); //5 ile tam bölünür mü diye sorulsaydı
        // işlem sonucu 0 olsaydı bölünür derdik.


    }
}
