package Day11_WhileLoop_DoWhileLoop;

public class C01_WhileLoop {

    public static void main(String[] args) {

        int sayi = 0;
        while (sayi<5 ){
            System.out.println("sayi : " + sayi);
            sayi++;
        }

        System.out.println("   ");

        for (int i = 0; i <5 ; i++) {
            System.out.println("sayi : " + i);

        }
    }

}
